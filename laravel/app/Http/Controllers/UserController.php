    public function userBlockage(UserBlockageRequest $request, $userId)
    {
        $user = User::findOrFail($userId);

        $done = $user->getIsBlocked()
            ? $user->unblock()
            : $user->block($request->get('reason'));

        return response()->json([
            'status' => $done,
            'blocked' => $user->getIsBlocked()
        ]);
    }
